def generate_articles() -> list[Article]:
    """記事データを生成して返す"""
    articles = []
    now = datetime.now()

    for template in ARTICLE_TEMPLATES:
        products = [Product(**p) for p in template.get("products", [])]
        article = Article(
            slug=template["slug"],
            title=template["title"],
            meta_description=template["meta_description"],
            keyword=template["keyword"],
            h1=template["h1"],
            intro=template["intro"],
            sections=template["sections"],
            products=products,
            published_at=now,
            updated_at=now,
            faq=template.get("faq", [])
        )
        articles.append(article)

    return articles


def save_articles_as_json(articles: list[Article]):
    """記事データをJSONファイルに保存"""
    os.makedirs(ARTICLES_DIR, exist_ok=True)
    for article in articles:
        filepath = os.path.join(ARTICLES_DIR, f"{article.slug}.json")
        with open(filepath, "w", encoding="utf-8") as f:
            json.dump(article.model_dump(mode="json"), f, ensure_ascii=False, indent=2)


def load_articles_from_json() -> list[Article]:
    """JSONファイルから記事データを読み込み"""
    articles = []
    if not os.path.exists(ARTICLES_DIR):
        return articles
    for filename in sorted(os.listdir(ARTICLES_DIR)):
        if filename.endswith(".json"):
            filepath = os.path.join(ARTICLES_DIR, filename)
            with open(filepath, "r", encoding="utf-8") as f:
                data = json.load(f)
                articles.append(Article(**data))
    return articles


if __name__ == "__main__":
    articles = generate_articles()
    save_articles_as_json(articles)
    print(f"Generated {len(articles)} articles:")
    for a in articles:
        print(f"  - {a.slug}: {a.title}")
